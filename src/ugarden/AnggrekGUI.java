    private void buttonSiramActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSiramActionPerformed
        // TODO add your handling code here:
        if(anggrek.getJumlahAir()<1)
        {
            JOptionPane.showMessageDialog(null, "Air Habis, tambahkan air dulu!");
        }
        else
        {
            anggrek.siramAir();
            refreshStatusPertumbuhan();
            textSisaAir.setText(String.valueOf(anggrek.getJumlahAir()));
            textStatusSiram.setText(String.valueOf(anggrek.getStatusPenyiraman()));
        }
    }//GEN-LAST:event_buttonSiramActionPerformed

    private void buttonBeriPupukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBeriPupukActionPerformed
        // TODO add your handling code here:
        if(anggrek.getJumlahPupuk()<1)
        {
            JOptionPane.showMessageDialog(null, "Pupuk Habis, tambahkan pupuk dulu!");
        }
        else
        {
            anggrek.beriPupuk();
            refreshStatusPertumbuhan();
            textSisaPupuk.setText(String.valueOf(anggrek.getJumlahPupuk()));
            textStatusPupuk.setText(String.valueOf(anggrek.getStatusPemupukan()));
        }
    }//GEN-LAST:event_buttonBeriPupukActionPerformed

