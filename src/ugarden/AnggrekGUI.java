    private String getResourceGambar()
    {
        String result = "";
        switch(anggrek.getStatusTumbuhInteger())
        {
            case 0:
                result = "/img/benih.jpg";
            break;
            case 1:
                result = "/img/tunas.jpg";
            break;
            case 2:
                result = "/img/kecil.jpg";
            break;
            case 3:
                result = "/img/besar.jpg";
            break;
            case 4:
                result = "/img/bunga.jpg";
            break;
            default :
                result = "/img/bunga.jpg";
            break;
        }
        return result;
    }
    private void refreshStatusPertumbuhan()
    {
        if(anggrek.isTumbuh())
        {
            anggrek.bertumbuh();
            JOptionPane.showMessageDialog(null, "Selamat, tanaman anda bertambah besar");
            labelImage.setIcon(new javax.swing.ImageIcon(getClass().getResource(getResourceGambar())));// ubah image
            labelStatusPertumbuhan.setText(anggrek.getStatusTumbuh());
        }
    }
    
